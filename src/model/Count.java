package model;

public class Count {
	double win = 0;
	double lose = 0;
	double draw = 0;
	public double getWin() {
		return win;
	}
	public double getLose() {
		return lose;
	}
	public double getDraw() {
		return draw;
	}
	public void setWin(double win) {
		this.win = win;
	}
	public void setLose(double lose) {
		this.lose = lose;
	}
	public void setDraw(double draw) {
		this.draw = draw;
	}
}
