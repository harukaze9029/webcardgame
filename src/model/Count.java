package model;

public class Count {
	double win = 0;
	double lose = 0;
	double drow = 0;
	public double getWin() {
		return win;
	}
	public double getLose() {
		return lose;
	}
	public double getDrow() {
		return drow;
	}
	public void setWin(double win) {
		this.win = win;
	}
	public void setLose(double lose) {
		this.lose = lose;
	}
	public void setDrow(double drow) {
		this.drow = drow;
	}
}
