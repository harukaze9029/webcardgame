package model;

public class Battle {
	double win = 0.0;
	double lose = 0.0;
	double draw = 0.0;
	public double getWin() {
		return win;
	}
	public double getLose() {
		return lose;
	}
	public double getDraw() {
		return draw;
	}
	public void setWin(double d) {
		this.win = d;
	}
	public void setLose(double d) {
		this.lose = d;
	}
	public void setDraw(double d) {
		this.draw = d;
	}

}
